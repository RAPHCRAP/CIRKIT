package com.example.logisim.util;

/**
 * Minimal SVG renderer placeholder used by the UI.
 * Provides a small helper to return an SVG string for export/testing.
 */
public class SvgRenderer {
    public static String renderPlaceholder() {
        return "<svg xmlns='http://www.w3.org/2000/svg' width='100' height='100'></svg>";
    }
}